// --- Extended type and helper functions ---

/**
 * Extended summary type for the discovery list view.
 * Adds `county` to DistrictSummary for filtering and display.
 * The core DistrictSummary type is unchanged — this extension lives in the mock layer.
 */
export interface DistrictListItem extends DistrictSummary {
  county: string;
}

export function getMockDistrictListItems(): DistrictListItem[] {
  return MOCK_DISTRICTS.map((d) => ({
    districtId: d.districtId,
    name: d.name,
    state: d.state,
    location: d.location,
    enrollment: d.totalEnrollment,
    county: d.county,
  }));
}

export function getMockCountyFilters(): FilterFacet {
  const countyCounts = new Map<string, number>();
  MOCK_DISTRICTS.forEach((d) => {
    countyCounts.set(d.county, (countyCounts.get(d.county) || 0) + 1);
  });

  const options: FilterOption[] = Array.from(countyCounts.entries())
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([county, count]) => ({
      value: county,
      label: `${county} (${count})`,
      count,
    }));

  return {
    filterName: 'county',
    filterLabel: 'County',
    options,
  };
}

export function getMockDistrictSummaries(): DistrictSummary[] {
  return MOCK_DISTRICTS.map((d) => ({
    districtId: d.districtId,
    name: d.name,
    state: d.state,
    location: d.location,
    enrollment: d.totalEnrollment,
  }));
}
